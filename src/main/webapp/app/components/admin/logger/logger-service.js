import { serverUrl } from '../../../utils/env'
import { request } from '../../../utils/request'

export default {
	fetchLoggers: () => {
		return request(`${serverUrl}management/loggers`)
	},
	changeLogLevel: (name, level) =>
		request(
			`${serverUrl}management/loggers/${name}`,
			'POST',
			JSON.stringify({ configuredLevel: level }),
			{
				'Content-Type': 'application/json',
			}
		),
}
