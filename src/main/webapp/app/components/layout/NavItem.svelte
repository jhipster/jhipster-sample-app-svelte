<script>
	import Icon from 'fa-svelte'
	import { stores } from '@sapper/app'

	export let icon
	export let label
	export let route

	const { page } = stores()

	$: textClass =
		$page && $page.path && $page.path === route
			? 'text-white'
			: 'text-gray-400'
</script>

<div class="px-2 py-1 sm:flex sm:p-0">
	<a
		href="{route}"
		on:click
		class="flex items-center px-2 py-1 sm:py-1 rounded font-semibold
			hover:text-white hover:bg-gray-700 focus:outline-none focus:ring {textClass}"
	>
		<Icon class="fa-icon mr-1" icon="{icon}" />
		{label}</a
	>
</div>
