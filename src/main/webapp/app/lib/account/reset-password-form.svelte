<script>
	import Button from '$lib/button.svelte'
	import Form from '$lib/page/form.svelte'
	import PasswordConfirm from '$lib/account/password-confirm.svelte'

	export let password = ''
	let validPassword = false
</script>

<Form testId="resetPwdForm">
	<PasswordConfirm
		label="New Password"
		value="{password}"
		on:input="{event => (password = event.detail.value)}"
		on:validate="{event => (validPassword = event.detail.valid)}"
	/>
	<Button type="submit" on:click disabled="{!validPassword}">
		Reset password
	</Button>
</Form>
