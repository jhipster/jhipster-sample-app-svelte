export function formatDate(dateToFormat) {
	if (dateToFormat) {
		const date = new Date(Date.parse(dateToFormat))
		return date.toLocaleDateString('en-US', {
			hour12: false,
			year: 'numeric',
			month: 'numeric',
			day: 'numeric',
			hour: 'numeric',
			minute: 'numeric',
		})
	}

	return '-'
}
